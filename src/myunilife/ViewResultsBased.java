    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/myunilife","root","");

            String year = (String)jComboBoxyear.getSelectedItem();
            String semester = (String)jComboBoxsemster.getSelectedItem();

            String sql1 = "select yid from year where year = '"+year+"'";
            Statement stm = con.createStatement();
            ResultSet rst = stm.executeQuery(sql1);
            while(rst.next()){
                yid = rst.getInt("yid");
            }

            String sql2 = "select semid from semester where semester = '"+semester+"'";
            Statement stm1 = con.createStatement();
            ResultSet rst1 = stm1.executeQuery(sql2);
            while(rst1.next()){
                semid= rst1.getInt("semid");
            }

            if((semid != 0) && (yid != 0)){
                //JOptionPane.showMessageDialog(null,"Asini");
                this.dispose();               
                
                InputStream resourceAsStream = getClass().getResourceAsStream("/reports/Simple_Blue.jasper");
                HashMap map = new HashMap();
                map.put("Parameter", yid);
                map.put("Parameter", semid);
                JasperPrint jasperPrint;
                try {            

                    jasperPrint = JasperFillManager.fillReport(resourceAsStream, map, con);
                    JasperViewer.viewReport(jasperPrint, false);
                } catch (JRException e) {
                    JOptionPane.showMessageDialog(null, e);
                } 
            }
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnaddActionPerformed

